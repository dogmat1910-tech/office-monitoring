CRITICAL_ERRORS = [
    "Отсутствие попытки продажи",
    "Дезинформация в соответствии с Расписанием Болезней (РБ) или 53-ФЗ (закон о воинской обязанности)",
]


def build_checklist_text() -> str:
    """Форматирует чеклист для вставки в промпт."""
    lines = []
    for section_id, section in CHECKLIST.items():
        lines.append(f"{section_id}. {section['name']}:")
        for item_id, item_text in section["items"].items():
            lines.append(f"   {item_id}. {item_text}")
    return "\n".join(lines)


def build_errors_text() -> str:
    return "\n".join(f"- {e}" for e in CRITICAL_ERRORS)


