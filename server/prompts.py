# Для backward compatibility
CHECKLIST = CHECKLIST_PRIMARY_SALE


def build_checklist_text(checklist: dict | None = None) -> str:
    """Форматирует один чеклист для вставки в промпт."""
    cl = checklist or CHECKLIST_PRIMARY_SALE
    lines = []
    for section_id, section in cl.items():
        lines.append(f"{section_id}. {section['name']}:")
        for item_id, item_text in section["items"].items():
            lines.append(f"   {item_id}. {item_text}")
    return "\n".join(lines)


def build_all_checklists_text() -> str:
    """Все три чеклиста для system prompt'а."""
    parts = []
    for key, cl in ALL_CHECKLISTS.items():
        parts.append(f"### Сценарий: {CHECKLIST_LABELS[key]} (scenario={key})\n")
        parts.append(build_checklist_text(cl))
        parts.append("")
    return "\n".join(parts)


def build_errors_text() -> str:
    return "\n".join(f"- {e}" for e in CRITICAL_ERRORS)


